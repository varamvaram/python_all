# Text Type: str
name = "Alice"

# Numeric Types: int, float, complex
age = 30
height = 1.75
c = 3 + 2j

# Sequence Types: list, tuple, range
numbers = [1, 2, 3, 4, 5]
coordinates = (10, 20)
num_range = range(1, 10)

# Mapping Type: dict
person = {'name': 'Bob', 'age': 25, 'city': 'London'}

# Set Types: set, frozenset
fruits = {'apple', 'banana', 'orange'}
unique_numbers = frozenset([1, 2, 3, 4, 5])

# Boolean Type: bool
is_student = True

# Binary Types: bytes, bytearray, memoryview
binary_data = bytes([0x41, 0x42, 0x43])
byte_array = bytearray(b"Hello")
memory_view = memoryview(byte_array)

# None Type: NoneType
result = None

# Printing the values
print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Complex number:", c)
print("Numbers:", numbers)
print("Coordinates:", coordinates)
print("Number range:", num_range)
print("Person:", person)
print("Fruits:", fruits)
print("Unique numbers:", unique_numbers)
print("Is student?", is_student)
print("Binary data:", binary_data)
print("Byte array:", byte_array)
print("Memory view:", memory_view)
print("Result:", result)